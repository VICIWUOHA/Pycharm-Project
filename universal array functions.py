import numpy as np
# arrange functiop

A = np.arange(15)
print(A)
A = np.arange(10, 100, 10)
print(A)

# sqrt

B = np.sqrt(A)
print("B is")
print(B)

# EXP

C = np.exp(A)
print("C is")
print(C)

# random
# addition
D = np.add(A, B)
print("D is")
print(D)

#maximum

E = np.maximum(B, D)
print("E is")
print(E)
